// Primeiro número
let num1 = 10;
// Segundo número
let num2 = 20;
// Chama a função e armazena o resultado
let resultado = somar(num1,num2);

// Exibe o resultado
console.log("A soma de " + num1 + "e" + num2 + "é:" + resultado);
